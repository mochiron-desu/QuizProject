    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        jTextArea1.setEditable(false);

        String database = null;
        database = "sports_quiz_bank";
        System.out.println(database);

        A.setEnabled(false);
        B.setEnabled(false);
        C.setEnabled(false);
        D.setEnabled(false);

        JOptionPane.showMessageDialog(this, "Select only the final option, do not select random!");

        for (int i = 0; i < 5; i++) {
            int questionNumber = (int) (Math.random() * (20 - 1 + 1) + 1);
            try {
                Connection con = DriverManager.getConnection(
                        "jdbc:mysql://localhost:3306/quiz", "root", "admin");
                Statement stmt = (Statement) con.createStatement();
                String query = null;
                if (qType.equals("sports")) {
                    query = "select * from sports_quiz_bank where index_no ='" + questionNumber + "';";
                }
                else if(qType.equals("science")){
                    query = "select * from science_quiz_bank where index_no ='" + questionNumber + "';";
                }
                else if(qType.equals("gk")){
                    query = "select * from gk_quiz_bank where index_no ='" + questionNumber + "';";
                }

                ResultSet rs = stmt.executeQuery(query);
                if (rs.next()) {
                    question[i] = rs.getString("question");
                    options[i][0] = rs.getString("optionA");
                    options[i][1] = rs.getString("optionB");
                    options[i][2] = rs.getString("optionC");
                    options[i][3] = rs.getString("optionD");
                    options[i][4] = rs.getString("answer");
                    System.out.println(options[i][4]);
                }

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
        System.out.println(question.length);
    }//GEN-LAST:event_formWindowOpened

