    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        buttonGroup1.clearSelection();

        if (jButton1.getText().equals("Start")) {
            jButton1.setText("Next");
            A.setEnabled(true);
            B.setEnabled(true);
            C.setEnabled(true);
            D.setEnabled(true);
        }
        if (jButton1.getText().equals("Done")) {
            try {
                Connection con = DriverManager.getConnection(
                        "jdbc:mysql://localhost:3306/quiz", "root", "admin");
                Statement stmt = (Statement) con.createStatement();
                String query = "insert into logs values('" + curUserId + "','" + correct + "','" + ((correct * 10) / question.length) + "',curdate());";
                stmt.executeUpdate(query);

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
            this.dispose();
            new mainMenu(curUser, curUserId).setVisible(true);
            index = 0;
            correct = 0;
        }
        if (index == question.length) {
            A.setEnabled(false);
            B.setEnabled(false);
            C.setEnabled(false);
            D.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Your score is " + ((correct * 10) / question.length));
            jButton1.setText("Done");

        } else {
            jTextArea1.setText(question[index]);
            A.setText(options[index][0]);
            B.setText(options[index][1]);
            C.setText(options[index][2]);
            D.setText(options[index][3]);

            if (index == question.length - 1) {
                jButton1.setText("Check your score now");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

